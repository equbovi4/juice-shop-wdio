class AddressData {
    constructor(country, name, mobileNumber, zipcode, address, city, state) {
        this.country = country;
        this.name = name;
        this.mobileNumber = mobileNumber,
        this.zipcode = zipcode,
        this.address = address,
        this.city = city,
        this.state = state
    }
}

module.exports = AddressData;